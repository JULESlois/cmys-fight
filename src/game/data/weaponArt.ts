export function getWeaponArtOffsets(weaponId: string): {
  renderOffsetX: number;
  renderOffsetY: number;
  muzzleOffsetX: number;
  muzzleOffsetY: number;
} | undefined {
  const sprite = WEAPON_SPRITES[weaponId];
  const anchor = WEAPON_ART_ANCHORS[weaponId];
  if (!sprite || !anchor) return undefined;
  const width = sprite[0]?.length ?? 0;
  const height = sprite.length;
  return {
    renderOffsetX: width / 2 - anchor.grip[0],
    renderOffsetY: height / 2 - anchor.grip[1],
    muzzleOffsetX: anchor.muzzle[0] - anchor.grip[0],
    muzzleOffsetY: anchor.muzzle[1] - anchor.grip[1],
  };
}
